//вКОЇIOГѕЪМЛЏХфжУ
void LCD_GPIO_Init(void)
{
	RCC->APB2ENR|=1<<3;
    RCC->APB2ENR|=1<<4;
    
    GPIOB->CRL&=0x0FFFFFFF;
    GPIOB->CRL|=0x30000000;
    
    GPIOB->CRH&=0xFFFF0FF0;
    GPIOB->CRH|=0x00003003;
    
    GPIOC->CRH&=0xFFFFF000;
    GPIOC->CRH|=0x00000333;
}


//ЯђSPIзмЯпДЋЪфвЛИі8ЮЛЪ§Он
void  SPI_WriteData(u8 Data)
{
	unsigned char i=0;
	for(i=8;i>0;i--)
	{
		if(Data&0x80)	
	  LCD_SDA_SET; //ЪфГіЪ§Он
      else LCD_SDA_CLR;
	   
      LCD_SCL_CLR;       
      LCD_SCL_SET;
      Data<<=1; 
	}
}

//ЯђвКОЇЦСаДвЛИі8ЮЛжИСю
void Lcd_WriteIndex(u8 Index)
{
   //SPI аДУќСюЪБађПЊЪМ
   LCD_CS_CLR;
   LCD_RS_CLR;
	 SPI_WriteData(Index);
   LCD_CS_SET;
}

//ЯђвКОЇЦСаДвЛИі8ЮЛЪ§Он
void Lcd_WriteData(u8 Data)
{
   LCD_CS_CLR;
   LCD_RS_SET;
   SPI_WriteData(Data);
   LCD_CS_SET; 
}
//ЯђвКОЇЦСаДвЛИі16ЮЛЪ§Он
void LCD_WriteData_16Bit(u16 Data)
{
   LCD_CS_CLR;
   LCD_RS_SET;
	 SPI_WriteData(Data>>8); 	//аДШыИп8ЮЛЪ§Он
	 SPI_WriteData(Data); 			//аДШыЕЭ8ЮЛЪ§Он
   LCD_CS_SET; 
}

void Lcd_WriteReg(u8 Index,u8 Data)
{
	Lcd_WriteIndex(Index);
  Lcd_WriteData(Data);
}

void Lcd_Reset(void)
{
	LCD_RST_CLR;
	delay_ms(100);
	LCD_RST_SET;
	delay_ms(50);
}

