/*
КЏЪ§ЙІФм: дкжИЖЈЮЛжУЯдЪОвЛИіжаЮФ
КЏЪ§ВЮЪ§: xСаЕФЦ№ЪМЮЛжУ(0~127)
				  yвГЕФЦ№ЪМЮЛжУ(0~7)
					sizeБэЪОзжЬхПэЖШ
					numberБэЪОвЊЯдЪОЕФЕкМИИіККзж(Ъ§зщЯТБъ)
ЫЕУї: ашвЊБЃжЄзжЬхКсЯђГпДчКЭзнЯђЪЧ8ЕФБЖЪ§ЁЃ
*/
void LCD_ShowChineseFont(u32 x,u32 y,u32 size,u8 *font,u16 c1,u16 c2)
{
		u32 i,j,x0=x;
		u8 tmp;
    u32 font_size=size*size/8; //ЕУЕНИУзжНкЕФзмзжНкДѓаЁ
  
    /*3. дкLCDЦСЩЯЯдЪОККзж*/
		for(i=0;i<font_size;i++)
		{
				tmp=*font++;
				for(j=0;j<8;j++)
				{
						if(tmp&0x80)Gui_DrawPoint(x,y,c1); //зжЬхбеЩЋ
						else Gui_DrawPoint(x,y,c2);    //БГОАбеЩЋ
						x++;
						tmp<<=1; //МЬајХаЖЯЯТвЛЮЛ
				}
				if(x-x0==size)
				{
						y++;  //знзјБъздді
						x=x0; //КсзјБъЙщЮЛ
				}
		}
}


