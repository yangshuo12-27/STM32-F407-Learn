/*************************************************
КЏЪ§УћЃКLCD_Set_Region
ЙІФмЃКЩшжУlcdЯдЪОЧјгђЃЌдкДЫЧјгђаДЕуЪ§ОнздЖЏЛЛаа
ШыПкВЮЪ§ЃКxyЦ№ЕуКЭжеЕу
ЗЕЛижЕЃКЮо
*************************************************/
void Lcd_SetRegion(u16 x_start,u16 y_start,u16 x_end,u16 y_end)
{		
Lcd_WriteIndex(0x2a);
	Lcd_WriteData(0x00);
	Lcd_WriteData(x_start+2);
	Lcd_WriteData(0x00);
	Lcd_WriteData(x_end+2);

	Lcd_WriteIndex(0x2b);
	Lcd_WriteData(0x00);
	Lcd_WriteData(y_start+1);
	Lcd_WriteData(0x00);
	Lcd_WriteData(y_end+1);
	
	Lcd_WriteIndex(0x2c);

}

