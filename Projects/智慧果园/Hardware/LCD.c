static void Gui_DrawChar16(u16 x, u16 y, u16 fc, u16 bc, u8 chr)
{
	u16 i;
	u16 j;
	u8 k;
	u8 tmp;

	k = chr;
	if (k > 32)
	{
		k -= 32;
	}
	else
	{
		k = 0;
	}

	Lcd_SetRegion(x, y, x + 7, y + 15);
	for (i = 0; i < 16; i++)
	{
		tmp = asc16[k * 16 + i];
		for (j = 0; j < 8; j++)
		{
			if (tmp & (0x80 >> j))
			{
				LCD_WriteData_16Bit(fc);
			}
			else
			{
				LCD_WriteData_16Bit(bc);
			}
		}
	}
}

void Gui_DrawFont_GBK16(u16 x, u16 y, u16 fc, u16 bc, u8 *s)
{
	u16 x0 = x;

	while (*s)
	{
		if ((*s) < 128)
		{
			if (*s == 13)
			{
				x = x0;
				y += 16;
			}
			else
			{
				Gui_DrawChar16(x, y, fc, bc, *s);
				x += 8;
			}
			s++;
		}
		else
		{
			s += 2;
			x += 16;
		}
	}
}
