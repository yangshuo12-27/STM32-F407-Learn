void LCD_ShowChineseFont(u32 x,u32 y,u32 size,u8 *font,u16 c1,u16 c2)
{
	u32 i;
	u32 j;
	u8 tmp;
	uint32_t primask;

	if (size != 16)
	{
		return;
	}

	primask = __get_PRIMASK();
	__disable_irq();

	LCD_CS_CLR;
	LCD_RS_CLR;
	SPI_WriteByteRaw(0x2a);
	LCD_CS_SET;
	LCD_CS_CLR;
	LCD_RS_SET;
	SPI_WriteByteRaw(0x00);
	SPI_WriteByteRaw((u8)(x + 2));
	SPI_WriteByteRaw(0x00);
	SPI_WriteByteRaw((u8)(x + 2 + 15));
	LCD_CS_SET;

	LCD_CS_CLR;
	LCD_RS_CLR;
	SPI_WriteByteRaw(0x2b);
	LCD_CS_SET;
	LCD_CS_CLR;
	LCD_RS_SET;
	SPI_WriteByteRaw(0x00);
	SPI_WriteByteRaw((u8)(y + 1));
	SPI_WriteByteRaw(0x00);
	SPI_WriteByteRaw((u8)(y + 1 + 15));
	LCD_CS_SET;

	LCD_CS_CLR;
	LCD_RS_CLR;
	SPI_WriteByteRaw(0x2c);
	LCD_CS_SET;

	LCD_CS_CLR;
	LCD_RS_SET;
	for (i = 0; i < 32; i++)
	{
		tmp = font[i];
		for (j = 0; j < 8; j++)
		{
			if (tmp & 0x80)
			{
				SPI_WriteByteRaw(c1 >> 8);
				SPI_WriteByteRaw(c1);
			}
			else
			{
				SPI_WriteByteRaw(c2 >> 8);
				SPI_WriteByteRaw(c2);
			}
			tmp <<= 1;
		}
	}
	LCD_CS_SET;
	__set_PRIMASK(primask);
}


