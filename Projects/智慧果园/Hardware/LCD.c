void LCD_FillRect(u16 x, u16 y, u16 w, u16 h, u16 color)
{
	u16 x_end;
	u16 y_end;
	u32 total;
	u32 i;

	if (w == 0 || h == 0)
	{
		return;
	}

	x_end = x + w - 1;
	y_end = y + h - 1;
	if (x_end >= X_MAX_PIXEL)
	{
		x_end = X_MAX_PIXEL - 1;
	}
	if (y_end >= Y_MAX_PIXEL)
	{
		y_end = Y_MAX_PIXEL - 1;
	}

	Lcd_SetRegion(x, y, x_end, y_end);
	total = (u32)(x_end - x + 1) * (u32)(y_end - y + 1);
	for (i = 0; i < total; i++)
	{
		LCD_WriteData_16Bit(color);
	}
}

