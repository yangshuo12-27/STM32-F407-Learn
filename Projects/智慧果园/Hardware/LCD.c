void Lcd_SetRegion(u16 x_start,u16 y_start,u16 x_end,u16 y_end)
{
	uint32_t primask;

	primask = __get_PRIMASK();
	__disable_irq();
	LCD_CS_CLR;
	LCD_RS_CLR;
	SPI_WriteByteRaw(0x2a);
	LCD_CS_SET;

	LCD_CS_CLR;
	LCD_RS_SET;
	SPI_WriteByteRaw(0x00);
	SPI_WriteByteRaw(x_start + 2);
	SPI_WriteByteRaw(0x00);
	SPI_WriteByteRaw(x_end + 2);
	LCD_CS_SET;

	LCD_CS_CLR;
	LCD_RS_CLR;
	SPI_WriteByteRaw(0x2b);
	LCD_CS_SET;

	LCD_CS_CLR;
	LCD_RS_SET;
	SPI_WriteByteRaw(0x00);
	SPI_WriteByteRaw(y_start + 1);
	SPI_WriteByteRaw(0x00);
	SPI_WriteByteRaw(y_end + 1);
	LCD_CS_SET;

	LCD_CS_CLR;
	LCD_RS_CLR;
	SPI_WriteByteRaw(0x2c);
	LCD_CS_SET;
	__set_PRIMASK(primask);
}

